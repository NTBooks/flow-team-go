module.exports = {

    randomPropertyName: () => {
        var ans = "";
        while (ans == "" || ans.length > 14) {

            const rolls = [Math.floor(Math.random() * cities.length),
            Math.floor(Math.random() * animalNames.length),
            Math.floor(Math.random() * 39) + 60
            ];

            var part1 = cities[rolls[0]];
            var part2 = animalNames[rolls[1]];
            ans = rolls[2] + part1 + part2;
            plainText = `'${rolls[2]} ${part1} ${part2}`
        }
        return ans + "," + plainText;
    },

    verifyPropertyName: (name) => {

        const vname = name.indexOf(",") > 0 ? name.split(",")[0] : name;

        try {

            if (!/^[6-9][0-9][A-Z][a-z]{2,}[A-Z][a-z]{2,}([A-Z][a-z]{2,})*$/.test(vname))
                return false;

            return true;
        } catch (ex) {
            console.log(ex);
            return false;
        }

    }
}